namespace BenBurgers.Python.Grammar.Operators;

/// <summary>
/// Python operators.
/// </summary>
public static partial class PythonOperators
{
    /// <summary>
    /// Arithmetic power operator.
    /// </summary>
    public const string ArithmeticPower = "**";

    /// <summary>
    /// Assignment operator.
    /// </summary>
    public const string Assignment = "=";

    /// <summary>
    /// Bitwise 'and' operator.
    /// </summary>
    public const string BitwiseAnd = "&";

    /// <summary>
    /// Bitwise 'or' operator.
    /// </summary>
    public const string BitwiseOr = "|";

    /// <summary>
    /// Bitwise exclusive 'or' operator.
    /// </summary>
    public const string BitwiseXor = "^";

    /// <summary>
    /// A decimal separator.
    /// </summary>
    public const string DecimalSeparator = ".";

    /// <summary>
    /// The end statement character.
    /// </summary>
    public const string EndStatement = ";";

    /// <summary>
    /// A list separator.
    /// </summary>
    public const string ListSeparator = ",";

    /// <summary>
    /// The member access operator.
    /// </summary>
    public const string MemberAccess = ".";
}
